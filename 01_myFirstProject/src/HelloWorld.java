

public class HelloWorld { // 클래스
	
	// {}블럭 안에 새로운 구문을 작성하고자 한다면 한 탭 들여써주는게 매너!!
	
	// 클래스 영역안에는 여러개의 메소드를 정의해둘 수 있다! (메소드 == 기능)
	
	// 메인메소드 => 해당 클래스 실행시 제일 먼저 찾아서 실행(호출)되는 메소드
	public static void main(String[] args) {
		
		// 어떤 값을 화면에 출력하고자 할때 출력문을 통해 작업 (print, println, printf)
		
		// System.out.println(출력하고자하는값); --> 해당 값을 출력한 후 "한 줄 띄어주는" 역할 수행
		System.out.println("Hi\nEveryOne!!");
		
		// System.out.print(출력하고자하는값); --> 해당 값을 출력만 하고 끝 --> "한 줄 띄워주는"역할 수행x
		System.out.print("안녕하세요!\n");
		
		System.out.print("여러분~!");
		
	}
	
	// 자바 클래스들을 보관하는 폴더 == 패키지(package)
	// 각 주제별 클래스들을 패키지를 따로따로 만들어서 관리해야만한다!
	
	// 패키지명 : 소문자로 시작하는 걸 권장, 최대한 한 단어로만 기술하는걸 권장
	//          2번째 레벨까지 보통 도메인의 역순으로 지음!

}









